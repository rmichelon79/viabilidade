const AVAILABLE = Object.keys(CURVES).map(Number).sort((a, b) => a - b)

export function getCurve(n) {
  if (CURVES[n]) return CURVES[n]
  // Find nearest available
  const nearest = AVAILABLE.reduce((prev, curr) =>
    Math.abs(curr - n) < Math.abs(prev - n) ? curr : prev
  )
  const base = CURVES[nearest]
  if (base.length === n) return base
  // Interpolate by resampling
  return resample(base, n)
}

function resample(curve, n) {
  const result = new Array(n).fill(0)
  const srcLen = curve.length
  for (let i = 0; i < n; i++) {
    const pos = (i / (n - 1)) * (srcLen - 1)
    const lo = Math.floor(pos)
    const hi = Math.min(lo + 1, srcLen - 1)
    const frac = pos - lo
    result[i] = curve[lo] * (1 - frac) + curve[hi] * frac
  }
  const sum = result.reduce((a, b) => a + b, 0)
  return result.map(v => v / sum)
}
